package bac_a_sable.tutos;

public class FonctionMain {

	// Le point de départ de tout programme Java est une fonction dans une classe
	//
	// Cette fonction est:
	public static void main(String[] args) {
		
		// c'est ici qu'on place le contenu du programme
		
		// pour afficher du texte à l'écran, on utilise cette syntaxe:
		System.out.println("Hello World !");
		
		// "System.out.println" -> c'est le nom de la fonction pour afficher quelque chose.
		// entre parenthèses, on a le texte (ou les objets) a afficher
		
		System.out.println(5);
		System.out.println("10 + 5 " + (10 + 5));
	}
}
